#  Store a list of data in a variable iterable_variable.
iterable_variable = [1, 2, 3, 4, 5, 6]
# Set a value 0 in variable total.
total = 0
# For each item in iterable_variable
for item in iterable_variable:
    # calculate total + item
    total = total + item
    # Output the result after each item have been iterated over.
    print(total)